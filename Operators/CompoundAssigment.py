a = 10 ;
b = 5;

print("Compound Assignment Operators in Python");

a += b; # a = a + b; 
print("After a += b, a = {}".format(a));

a -= b; # a = a - b;
print("After a -= b , a = {} ".format(a));

a *= b; # a = a * b;
print("After a *= b , a = {} ".format(a));

a /= b; # a = a / b;
print("After a /= b , a = {} ".format(a));

a %= b; # a = a % b;
print("After a %= b , a = {} ".format(a)); 
