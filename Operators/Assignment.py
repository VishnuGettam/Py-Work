#Assignment Operators in Python
x = 5; 
y = "Alice";

print (" x = {} and y = {}".format(x,y));

#Single value multiple assignment
a = b = c = 10;

print("a = {} and b = {} and c = {}".format(a, b, c));

#Multiple value assignment
x,y,z = 6, "Bob", 3.5;

print("x = {} and y = {} and z = {}".format(x, y, z));