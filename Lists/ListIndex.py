
#List numbers
numbers = [10, 20, 30, 40, 50];

#Forward Indexing [0:1:2:3:4:5]
#Accessing elements using index
print("First Element : {}".format(numbers[0]));

print("First Element : {} && Third Element : {}".format(numbers[0],numbers[2]));

#Negative Indexing [-5:-4:-3:-2:-1]

print("Last Element : {}".format(numbers[-1]));
print("Last Element : {} && Second Element : {}".format(numbers[-1],numbers[-4]));