#Escape Sequence Examples

#New Line
print("Hello\nWorld");

#tab space
print("Hello\tWorld");

#backslash
print("This is a backslash: \\");

#single quote
print("Hello \'World\'")

#double quote
print("Hello \"World\"");

#r prefix to ignore escape sequences
print(r"This is a raw string: C:\newfolder\testfile.txt");